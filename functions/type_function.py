'''
Typecasting
Convert datatype of variable into another
'''

#integer to string
default_variable_integer=3465
print(type(default_variable_integer)) # print datatype of variable the default variable
default_variable_integer=str(default_variable_integer) #connvert into string variable
print(default_variable_integer, "", "is", "", "now", "", type(default_variable_integer)) # print datatype of variable the default variable

#string to integer and float
default_variable_string="565"
print(type(default_variable_string))
default_variable_string=int(default_variable_string) # convert into integer
print(default_variable_string, "", "is", "", "now", "", type(default_variable_string))
default_variable_string=float(default_variable_string) # convert into float
print(default_variable_string, "", "is", "", "now", "", type(default_variable_string))