# power and exponents
# square of a number : number^2 --> number*number

# define an integer number to varibale
number=27
# square --> number^2
square_number=number**2
print(square_number)
# square root --> number^(1/2) --> number^0.5
squareRoot_number=number**0.5
print(squareRoot_number)
# cube --> number^3
cube_number=number**3
print(cube_number)
# cube root --> number^(1/3)
cubeRoot_number=number**(1/3) # returns float
print(cubeRoot_number)

