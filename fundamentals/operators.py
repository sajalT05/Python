assignedvalue/=1 #divide 1
print(assignedvalue)

# Comparison operators
# Returns a boolean
comparedvalue1=(5==5)
comparedvalue2=(7>5)
comparedvalue3=(1>5)
comparedvalue4=(5<6)
comparedvalue5=(9<4)
print(comparedvalue1,comparedvalue2,comparedvalue3,comparedvalue4,comparedvalue5)

# Logical operators
logicalvalue1=True #logicalvalue1 is a true boolean
logicalvalue2=False #logicalvalue2 is a false boolean
print(logicalvalue1 and logicalvalue2) #returns true when both booleans are true
print(logicalvalue1 or logicalvalue2) #returns true when any one of the boolean is true
print(not logicalvalue2) #returns true when boolean is false